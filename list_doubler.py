# Original list
numbers = [1, 3, 5, 7, 9]

# New list to store doubled values
doubled_numbers = []

# Loop to double each element
for num in numbers:
    doubled_numbers.append(num * 2)

# Print results
print(f"Original List: {numbers}")
print(f"New List: {doubled_numbers}")
